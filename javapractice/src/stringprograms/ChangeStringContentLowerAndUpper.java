package stringprograms;

public class ChangeStringContentLowerAndUpper {
public static void main(String[] args) {
	String firstname="yeti.venkata jaswanth ram";
	
	String changeToUpperCase=firstname.toUpperCase();
	
	System.out.println(changeToUpperCase);
	
	
	String secondName="YETI.VENKATA JASWANTH RAM";
	
	
	String changeToLowerCase=secondName.toLowerCase();
	System.out.println(changeToLowerCase);
}
}
