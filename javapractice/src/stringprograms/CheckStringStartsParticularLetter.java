package stringprograms;

public class CheckStringStartsParticularLetter {
public static void main(String[] args) {
	String firstName="chennakesava   ";
	System.out.println(firstName.startsWith("c"));
	System.out.println(firstName.startsWith("h"));
	System.out.println(firstName.endsWith("a"));
	System.out.println(firstName.strip()+"tirumala");
}
}
